const worldwide = () => {
    const worldwide = document.querySelector('.worldwide');
    const image = document.querySelector('.worldwideimg');
    const para = document.querySelector('.worldwidep')
    
    worldwide.addEventListener('mouseenter',() => {
        
        image.classList.toggle('image-active');
        para.classList.toggle('para-active');
        
    });

    worldwide.addEventListener('mouseleave',() => {
        
        image.classList.toggle('image-active');
        para.classList.toggle('para-active');

        
    });
    

}

worldwide();
